def _is_probably_csv_url(url: str) -> bool:
    parsed = urllib.parse.urlparse(url)
    path = parsed.path.lower()
    return path.endswith(".csv") or ".csv" in url.lower()


def _parse_huggingface_dataset_id(url: str) -> str | None:
    parsed = urllib.parse.urlparse(url)
    if (parsed.hostname or "").lower() != "huggingface.co":
        return None
    parts = [part for part in parsed.path.split("/") if part]
    if len(parts) < 3 or parts[0] != "datasets":
        return None
    return f"{parts[1]}/{parts[2]}"


def _huggingface_resolve_url(dataset_id: str, path: str) -> str:
    encoded_parts = [urllib.parse.quote(part, safe="") for part in dataset_id.split("/")]
    encoded_path = "/".join(urllib.parse.quote(part, safe="") for part in path.split("/"))
    return f"https://huggingface.co/datasets/{'/'.join(encoded_parts)}/resolve/main/{encoded_path}"


async def _fetch_huggingface_csv_links(url: str) -> tuple[str | None, list[str]]:
    dataset_id = _parse_huggingface_dataset_id(url)
    if not dataset_id:
        return None, []
    dataset_api = f"https://huggingface.co/api/datasets/{dataset_id}"
    tree_api = f"https://huggingface.co/api/datasets/{dataset_id}/tree/main?recursive=true"
    async with httpx.AsyncClient(follow_redirects=True, timeout=30.0) as client:
        info_response = await client.get(dataset_api)
        info_response.raise_for_status()
        info_payload = info_response.json()
        tree_response = await client.get(tree_api)
        tree_response.raise_for_status()
        tree_payload = tree_response.json()

    page_title = info_payload.get("id") or dataset_id
    csv_urls: list[str] = []

    for sibling in info_payload.get("siblings", []) or []:
        path = sibling.get("rfilename")
        if isinstance(path, str) and path.lower().endswith(".csv"):
            csv_urls.append(_huggingface_resolve_url(dataset_id, path))

    for entry in tree_payload or []:
        path = entry.get("path")
        entry_type = entry.get("type")
        if entry_type == "file" and isinstance(path, str) and path.lower().endswith(".csv"):
            csv_urls.append(_huggingface_resolve_url(dataset_id, path))

    return page_title, list(dict.fromkeys(csv_urls))


async def _fetch_html_and_csv_links(url: str) -> tuple[str | None, list[str]]:
    hf_title, hf_csv_urls = await _fetch_huggingface_csv_links(url)
    if hf_csv_urls:
        return hf_title, hf_csv_urls
    async with httpx.AsyncClient(follow_redirects=True, timeout=30.0) as client:
        response = await client.get(url)
        response.raise_for_status()
        parser = CsvLinkParser(str(response.url))
        parser.feed(response.text)
        csv_urls = list(dict.fromkeys(parser.csv_urls))
        return parser.page_title or None, csv_urls


async def _stream_download_csv(url: str, temp_dir: Path) -> Path:
    parsed = urllib.parse.urlparse(url)
    filename = Path(parsed.path).name or f"download-{uuid.uuid4().hex}.csv"
    destination = temp_dir / filename
    async with httpx.AsyncClient(follow_redirects=True, timeout=60.0) as client:
        async with client.stream("GET", url) as response:
            response.raise_for_status()
            content_length = response.headers.get("Content-Length")
            if content_length:
                declared = int(content_length)
                if declared < MIN_FILE_BYTES:
                    raise ValueError(f"CSV rejected: Content-Length {declared} < {MIN_FILE_BYTES}")
                if declared > MAX_FILE_BYTES:
                    raise ValueError(f"CSV rejected: Content-Length {declared} > {MAX_FILE_BYTES}")
            total = 0
            with destination.open("wb") as handle:
                async for chunk in response.aiter_bytes():
                    if not chunk:
                        continue
                    total += len(chunk)
                    if total > MAX_FILE_BYTES:
                        raise ValueError(f"CSV rejected: streamed size exceeded {MAX_FILE_BYTES}")
                    handle.write(chunk)
    size = destination.stat().st_size
    if size < MIN_FILE_BYTES:
        raise ValueError(f"CSV rejected: file size {size} < {MIN_FILE_BYTES}")
    return destination


def _read_csv_preview(path: Path) -> tuple[list[str], list[dict[str, Any]]]:
    df = pd.read_csv(path, nrows=5)
    headers = [str(column).strip() for column in df.columns.tolist() if str(column).strip()]
    if not headers:
        raise ValueError("CSV rejected: no valid headers found")
    records = json.loads(df.fillna("").to_json(orient="records"))
    return headers, records


def _prepare_staging_paths(job_id: int, csv_url: str) -> tuple[Path, Path]:
    suffix = Path(urllib.parse.urlparse(csv_url).path).suffix or ".csv"
    stem = Path(urllib.parse.urlparse(csv_url).path).stem or "dataset"
    safe_stem = "".join(ch if ch.isalnum() or ch in {"-", "_"} else "_" for ch in stem)[:80] or "dataset"
    job_dir = INGESTION_STAGING_DIR / str(job_id)
    job_dir.mkdir(parents=True, exist_ok=True)
    unique = uuid.uuid4().hex[:8]
    csv_path = job_dir / f"{safe_stem}-{unique}{suffix}"
    schema_path = job_dir / f"{safe_stem}-{unique}.json"
    return csv_path, schema_path


async def _process_candidate_csv(
    conn: asyncpg.Connection,
    *,
    job_id: int,
    source_url: str,
    page_title: str | None,
    csv_url: str,
    requested_by: str | None,
    failures: list[str],
) -> bool:
    with tempfile.TemporaryDirectory(prefix="patra-ingestion-") as tmp:
        temp_dir = Path(tmp)
        try:
            downloaded = await _stream_download_csv(csv_url, temp_dir)
            headers, rows = await asyncio.to_thread(_read_csv_preview, downloaded)
            llm_validation_failure: str | None = None
            llm_draft_failure: str | None = None
            validation: AIValidationResult
            if INGESTION_LLM_ENABLED:
                try:
                    validation_payload = await asyncio.to_thread(
                        _call_llm_json_with_model_fallback,
                        prompt=_validation_prompt(
                            target_schema=DEFAULT_TARGET_SCHEMA,
                            page_title=page_title,
                            source_url=source_url,
                            csv_url=csv_url,
                            headers=headers,
                            rows=rows,
                        ),
                        schema_name="ai_validation_result",
                        schema=_validation_json_schema(),
                        api_base=INGESTION_LLM_API_BASE,
                        model=INGESTION_LLM_MODEL,
                        api_key=INGESTION_LLM_API_KEY,
                    )
                    validation = AIValidationResult.model_validate(validation_payload)
                except Exception as exc:  # noqa: BLE001
                    llm_validation_failure = str(exc)
                    if not INGESTION_LLM_FALLBACK_TO_CODE:
                        raise
                    validation = _heuristic_validation_result(
                        source_url=source_url,
                        page_title=page_title,
                        csv_url=csv_url,
                        headers=headers,
                        rows=rows,
                        llm_failure_reason=llm_validation_failure,
                    )
            else:
                validation = _heuristic_validation_result(
                    source_url=source_url,
                    page_title=page_title,
                    csv_url=csv_url,
                    headers=headers,
                    rows=rows,
                    llm_failure_reason="LLM validation disabled by configuration.",
                )
            if not validation.pass_:
                reason = "; ".join(validation.reject_reasons) or validation.summary
                failures.append(f"{csv_url}: {reason}")
                return False
            if INGESTION_LLM_ENABLED:
                try:
                    draft_payload = await asyncio.to_thread(
                        _call_llm_json_with_model_fallback,
                        prompt=_draft_prompt(
                            page_title=page_title,
                            source_url=source_url,
                            csv_url=csv_url,
                            headers=headers,
                            rows=rows,
                            validation_result=validation,
                        ),
                        schema_name="datasheet_draft",
                        schema=_draft_json_schema(),
                        api_base=INGESTION_LLM_API_BASE,
                        model=INGESTION_LLM_MODEL,
                        api_key=INGESTION_LLM_API_KEY,
                    )
                    draft = DatasheetDraftModel.model_validate(draft_payload)
                except Exception as exc:  # noqa: BLE001
                    llm_draft_failure = str(exc)
                    if not INGESTION_LLM_FALLBACK_TO_CODE:
                        raise
                    draft = _heuristic_draft(
                        source_url=source_url,
                        page_title=page_title,
                        csv_url=csv_url,
                        headers=headers,
                        rows=rows,
                        validation_result=validation,
                        llm_failure_reason=llm_draft_failure or llm_validation_failure,
                    )
            else:
                draft = _heuristic_draft(
                    source_url=source_url,
                    page_title=page_title,
                    csv_url=csv_url,
                    headers=headers,
                    rows=rows,
                    validation_result=validation,
                    llm_failure_reason="LLM draft generation disabled by configuration.",
                )
            staged_csv_path, staged_schema_path = _prepare_staging_paths(job_id, csv_url)
            staged_csv_path.write_bytes(downloaded.read_bytes())
            staged_schema_path.write_text(
                json.dumps(
                    {
                        "validation_result": validation.model_dump(by_alias=True),
                        "datasheet_draft": draft.model_dump(),
                        "headers_sample": headers,
                        "rows_sample": rows,
                    },
                    indent=2,
                ),
                encoding="utf-8",
            )
            await _insert_artifact(
                conn,
                job_id=job_id,
                source_url=source_url,
                page_title=page_title,
                csv_url=csv_url,
                headers_sample=headers,
                rows_sample=rows,
                validation_result=validation,
                datasheet_draft=draft,
                staged_csv_path=str(staged_csv_path),
                staged_schema_path=str(staged_schema_path),
                created_by=requested_by,
            )
            return True
        except (ParserError, UnicodeDecodeError, ValueError, httpx.HTTPError, json.JSONDecodeError) as exc:
            failures.append(f"{csv_url}: {exc}")
            return False
        except Exception as exc:  # noqa: BLE001
            log.exception("Unexpected automated ingestion failure for %s", csv_url)
            failures.append(f"{csv_url}: {exc}")
            return False


async def _run_scrape_job(pool: asyncpg.Pool, job_id: int, source_url: str, requested_by: str | None) -> None:
    failures: list[str] = []
    created_artifact_count = 0
    processed_csv_count = 0
    discovered_csv_urls: list[str] = []
    try:
        INGESTION_STAGING_DIR.mkdir(parents=True, exist_ok=True)
        async with pool.acquire() as conn:
            await _update_job(conn, job_id, status="Crawling")

        if _is_probably_csv_url(source_url):
            page_title = Path(urllib.parse.urlparse(source_url).path).name or "CSV resource"
            discovered_csv_urls = [source_url]
        else:
            page_title, discovered_csv_urls = await _fetch_html_and_csv_links(source_url)

        async with pool.acquire() as conn:
            await _update_job(
                conn,
                job_id,
                page_title=page_title,
                discovered_csv_urls=discovered_csv_urls,
                discovered_csv_count=len(discovered_csv_urls),
                status="AI_Validating" if discovered_csv_urls else "Failed",
                error_message="No CSV links discovered on the source page." if not discovered_csv_urls else None,
                recent_failures=failures,
                completed=not discovered_csv_urls,
            )
        if not discovered_csv_urls:
            return

        for csv_url in discovered_csv_urls:
            processed_csv_count += 1
            async with pool.acquire() as conn:
                passed = await _process_candidate_csv(
                    conn,
                    job_id=job_id,
                    source_url=source_url,
                    page_title=page_title,
                    csv_url=csv_url,
                    requested_by=requested_by,
                    failures=failures,
                )
            if passed:
                created_artifact_count += 1
            async with pool.acquire() as conn:
                await _update_job(
                    conn,
                    job_id,
                    processed_csv_count=processed_csv_count,
                    passed_csv_count=created_artifact_count,
                    created_artifact_count=created_artifact_count,
                    recent_failures=failures[-10:],
                )

        final_status = "Completed" if created_artifact_count > 0 else "Failed"
        final_error = None if created_artifact_count > 0 else "No CSV candidates passed validation."
        async with pool.acquire() as conn:
            await _update_job(
                conn,
                job_id,
                status=final_status,
                processed_csv_count=processed_csv_count,
                passed_csv_count=created_artifact_count,
                created_artifact_count=created_artifact_count,
                recent_failures=failures[-10:],
                error_message=final_error,
                completed=True,
            )
    except Exception as exc:  # noqa: BLE001
        log.exception("Automated ingestion job %s failed", job_id)
        async with pool.acquire() as conn:
            await _update_job(
                conn,
                job_id,
                status="Failed",
                error_message=str(exc),
                recent_failures=(failures + [str(exc)])[-10:],
                completed=True,
            )
    finally:
        ACTIVE_INGESTION_TASKS.pop(job_id, None)


def _track_task(job_id: int, task: asyncio.Task) -> None:
    ACTIVE_INGESTION_TASKS[job_id] = task
    task.add_done_callback(lambda _: ACTIVE_INGESTION_TASKS.pop(job_id, None))


