def _extract_json(content: str) -> dict[str, Any]:
    content = content.strip()
    if content.startswith("{") and content.endswith("}"):
        return json.loads(content)
    start = content.find("{")
    end = content.rfind("}")
    if start == -1 or end == -1 or end <= start:
        raise ValueError(f"Model did not return JSON: {content[:200]}")
    return json.loads(content[start : end + 1])


def _build_chat_completions_url(api_base: str) -> str:
    base = (api_base or "").rstrip("/")
    if base.endswith("/chat/completions"):
        return base
    if base.endswith("/v1"):
        return f"{base}/chat/completions"
    return f"{base}/v1/chat/completions"


def _build_models_url(api_base: str) -> str:
    base = (api_base or "").rstrip("/")
    if base.endswith("/models"):
        return base
    if base.endswith("/v1"):
        return f"{base}/models"
    return f"{base}/v1/models"


def _extract_message_text(message: Any) -> str:
    if isinstance(message, str):
        return message
    if isinstance(message, list):
        parts: list[str] = []
        for item in message:
            if isinstance(item, str):
                parts.append(item)
            elif isinstance(item, dict):
                text = item.get("text") or item.get("content") or ""
                if isinstance(text, str):
                    parts.append(text)
        return "\n".join(part for part in parts if part).strip()
    if isinstance(message, dict):
        text = message.get("text") or message.get("content") or ""
        return text if isinstance(text, str) else ""
    return ""


def _is_generation_model(model_id: str) -> bool:
    lowered = model_id.lower()
    return not any(token in lowered for token in ("embed", "embedding", "rerank", "moderation", "whisper", "tts"))


def _model_priority(model_id: str) -> tuple[int, str]:
    lowered = model_id.lower()
    if "gemma" in lowered:
        return (0, lowered)
    if "qwen" in lowered:
        return (1, lowered)
    if "glm" in lowered:
        return (2, lowered)
    return (3, lowered)


def _list_available_models(
    *,
    api_base: str,
    api_key: str,
    timeout_seconds: int = 10,
) -> list[str]:
    headers = {}
    if api_key:
        headers["Authorization"] = f"Bearer {api_key}"
    with httpx.Client(timeout=timeout_seconds) as client:
        response = client.get(_build_models_url(api_base), headers=headers)
        response.raise_for_status()
        payload = response.json()
    models: list[str] = []
    for item in payload.get("data", []):
        model_id = item.get("id")
        if isinstance(model_id, str) and model_id.strip() and _is_generation_model(model_id):
            models.append(model_id.strip())
    return sorted(dict.fromkeys(models), key=_model_priority)


def _call_llm_json(
    *,
    prompt: str,
    schema_name: str,
    schema: dict[str, Any],
    api_base: str = DEFAULT_LLM_API_BASE,
    model: str = DEFAULT_LLM_MODEL,
    api_key: str = DEFAULT_LLM_API_KEY,
    timeout_seconds: int = DEFAULT_LLM_TIMEOUT,
) -> dict[str, Any]:
    payload: dict[str, Any] = {
        "model": model,
        "temperature": 0.2,
        "top_p": 0.8,
        "presence_penalty": 0.5,
        "max_tokens": 1400,
        "chat_template_kwargs": {"enable_thinking": False},
        "messages": [
            {
                "role": "system",
                "content": (
                    "You are a PATRA data-ingestion validation agent. "
                    "Return strict JSON only. Do not include markdown, reasoning trace, or commentary."
                ),
            },
            {"role": "user", "content": prompt},
        ],
    }
    payload_with_schema = {
        **payload,
        "response_format": {
            "type": "json_schema",
            "json_schema": {
                "name": schema_name,
                "schema": schema,
            },
        },
    }
    headers = {
        "Content-Type": "application/json",
    }
    if api_key:
        headers["Authorization"] = f"Bearer {api_key}"
    with httpx.Client(timeout=timeout_seconds) as client:
        url = _build_chat_completions_url(api_base)
        try:
            response = client.post(
                url,
                headers=headers,
                json=payload_with_schema,
            )
            response.raise_for_status()
            parsed = response.json()
        except httpx.HTTPStatusError as exc:
            if exc.response.status_code != 400:
                raise
            response = client.post(
                url,
                headers=headers,
                json=payload,
            )
            response.raise_for_status()
            parsed = response.json()
    message = parsed["choices"][0]["message"]
    content = _extract_message_text(message.get("content"))
    if not content:
        reasoning = _extract_message_text(message.get("reasoning_content"))
        if reasoning:
            try:
                return _extract_json(reasoning)
            except Exception:
                pass
        raise ValueError(f"Model did not return JSON content. reasoning_content={reasoning[:400]}")
    try:
        return _extract_json(content)
    except Exception:
        reasoning = _extract_message_text(message.get("reasoning_content"))
        if reasoning:
            return _extract_json(reasoning)
        raise


def _call_llm_json_with_model_fallback(
    *,
    prompt: str,
    schema_name: str,
    schema: dict[str, Any],
    api_base: str,
    model: str | None,
    api_key: str,
    timeout_seconds: int = DEFAULT_LLM_TIMEOUT,
) -> dict[str, Any]:
    attempted: list[str] = []
    if model:
        candidate_models = [model]
    else:
        discovered = _list_available_models(api_base=api_base, api_key=api_key, timeout_seconds=min(timeout_seconds, 10))
        candidate_models = discovered or [DEFAULT_LLM_MODEL]

    last_exc: Exception | None = None
    for candidate in candidate_models:
        attempted.append(candidate)
        try:
            return _call_llm_json(
                prompt=prompt,
                schema_name=schema_name,
                schema=schema,
                api_base=api_base,
                model=candidate,
                api_key=api_key,
                timeout_seconds=timeout_seconds,
            )
        except Exception as exc:  # noqa: BLE001
            last_exc = exc
            log.warning("Ingestion LLM attempt failed for model %s: %s", candidate, exc)
            continue
    attempted_label = ", ".join(attempted) or "(none)"
    if last_exc:
        raise RuntimeError(f"All ingestion LLM attempts failed. attempted_models=[{attempted_label}] last_error={last_exc}") from last_exc
    raise RuntimeError(f"No ingestion LLM models available. attempted_models=[{attempted_label}]")


def _tokenize_header(value: str) -> set[str]:
    raw = "".join(ch.lower() if ch.isalnum() else " " for ch in str(value))
    return {token for token in raw.split() if token}


def _infer_header_mappings(headers: list[str], target_schema: dict[str, Any]) -> list[dict[str, str]]:
    mappings: list[dict[str, str]] = []
    target_fields = list(target_schema.keys())
    for header in headers:
        header_tokens = _tokenize_header(header)
        best_target = None
        best_score = 0
        for target in target_fields:
            target_tokens = _tokenize_header(target)
            overlap = len(header_tokens & target_tokens)
            if overlap > best_score:
                best_target = target
                best_score = overlap
        if best_target and best_score > 0:
            mappings.append(
                {
                    "source_header": header,
                    "target_field": best_target,
                    "rationale": "Deterministic token overlap between CSV header and PATRA target field.",
                }
            )
    return mappings


def _infer_resource_type(headers: list[str], rows: list[dict[str, Any]]) -> str:
    lowered = " ".join(headers).lower()
    if any(token in lowered for token in ("image", "label", "class", "target")):
        return "annotated_dataset"
    if any(token in lowered for token in ("lat", "lon", "longitude", "latitude", "geo")):
        return "geospatial_dataset"
    if any(token in lowered for token in ("date", "time", "timestamp", "year", "month")):
        return "tabular_timeseries_dataset"
    return "tabular_dataset"


def _infer_license_guess(source_url: str, page_title: str | None) -> str | None:
    text = f"{source_url} {page_title or ''}".lower()
    if "huggingface.co" in text:
        return "Unknown (check dataset card / repository metadata)"
    if "github.com" in text or "raw.githubusercontent.com" in text:
        return "Unknown (check repository license)"
    return None


def _infer_publisher(source_url: str) -> str | None:
    hostname = urllib.parse.urlparse(source_url).hostname or ""
    if hostname.endswith("huggingface.co"):
        return "Hugging Face"
    if hostname.endswith("githubusercontent.com") or hostname.endswith("github.com"):
        return "GitHub-hosted source"
    if hostname:
        return hostname.removeprefix("www.")
    return None


def _sample_quality_score(headers: list[str], rows: list[dict[str, Any]]) -> float:
    if not headers or not rows:
        return 0.0
    non_empty_cells = 0
    total_cells = 0
    for row in rows:
        for header in headers:
            total_cells += 1
            value = row.get(header)
            if value not in (None, "", "nan", "NaN"):
                non_empty_cells += 1
    density = non_empty_cells / total_cells if total_cells else 0.0
    width_bonus = min(len(headers) / 10.0, 0.2)
    row_bonus = min(len(rows) / 5.0, 0.1)
    return max(0.0, min(1.0, density * 0.7 + width_bonus + row_bonus))


def _heuristic_validation_result(
    *,
    source_url: str,
    page_title: str | None,
    csv_url: str,
    headers: list[str],
    rows: list[dict[str, Any]],
    llm_failure_reason: str | None = None,
) -> AIValidationResult:
    mappings = _infer_header_mappings(headers, DEFAULT_TARGET_SCHEMA)
    quality = _sample_quality_score(headers, rows)
    if len(headers) < 2 or not rows:
        reject_reasons = ["Deterministic fallback rejected the CSV because it has too few headers or no sample rows."]
        if llm_failure_reason:
            reject_reasons.append(f"LLM unavailable: {llm_failure_reason}")
        return AIValidationResult.model_validate(
            {
                "pass": False,
                "confidence": max(0.0, min(0.4, quality)),
                "resource_type": None,
                "field_mappings": [],
                "summary": "CSV rejected by deterministic fallback validation.",
                "license_guess": _infer_license_guess(source_url, page_title),
                "recommended_title": None,
                "reject_reasons": reject_reasons,
            }
        )
    summary = "Validated by deterministic fallback because LLM validation was unavailable."
    if llm_failure_reason:
        summary = f"{summary} LLM issue: {llm_failure_reason}"
    return AIValidationResult.model_validate(
        {
            "pass": True,
            "confidence": max(0.45, quality),
            "resource_type": _infer_resource_type(headers, rows),
            "field_mappings": mappings,
            "summary": summary,
            "license_guess": _infer_license_guess(source_url, page_title),
            "recommended_title": (page_title or Path(urllib.parse.urlparse(csv_url).path).stem or "CSV resource").strip(),
            "reject_reasons": [],
        }
    )


def _heuristic_draft(
    *,
    source_url: str,
    page_title: str | None,
    csv_url: str,
    headers: list[str],
    rows: list[dict[str, Any]],
    validation_result: AIValidationResult,
    llm_failure_reason: str | None = None,
) -> DatasheetDraftModel:
    title = validation_result.recommended_title or (page_title or Path(urllib.parse.urlparse(csv_url).path).stem or "CSV resource")
    top_headers = headers[:8]
    notes = [
        f"Headers sampled: {', '.join(top_headers)}" if top_headers else "Headers sampled from CSV.",
        "Draft generated via deterministic fallback.",
    ]
    if llm_failure_reason:
        notes.append(f"LLM unavailable during draft generation: {llm_failure_reason}")
    subjects = list(dict.fromkeys([token for header in headers[:10] for token in list(_tokenize_header(header))[:2]]))[:8]
    return DatasheetDraftModel.model_validate(
        {
            "title": title,
            "description": (
                f"CSV resource discovered from {page_title or source_url}. "
                f"The sample contains {len(headers)} headers and {len(rows)} preview rows."
            ),
            "resource_type": validation_result.resource_type or "tabular_dataset",
            "creators": [],
            "subjects": subjects,
            "publisher": _infer_publisher(source_url),
            "license_guess": validation_result.license_guess,
            "potential_uses": [
                "Initial admin review of externally discovered tabular data.",
                "Schema inspection and downstream PATRA resource curation.",
            ],
            "related_download_url": csv_url,
            "sample_notes": notes,
        }
    )


