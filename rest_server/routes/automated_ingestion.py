class CsvLinkParser(HTMLParser):
    def __init__(self, base_url: str) -> None:
        super().__init__()
        self.base_url = base_url
        self.page_title = ""
        self._inside_title = False
        self._title_chunks: list[str] = []
        self.csv_urls: list[str] = []

    def handle_starttag(self, tag: str, attrs: list[tuple[str, str | None]]) -> None:
        lowered = tag.lower()
        attrs_dict = {key.lower(): value for key, value in attrs}
        if lowered == "title":
            self._inside_title = True
            self._title_chunks = []
            return
        if lowered != "a":
            return
        href = attrs_dict.get("href")
        if not href:
            return
        resolved = urllib.parse.urljoin(self.base_url, href)
        parsed = urllib.parse.urlparse(resolved)
        if parsed.scheme not in {"http", "https"}:
            return
        if parsed.path.lower().endswith(".csv") or ".csv" in resolved.lower():
            self.csv_urls.append(resolved)

    def handle_endtag(self, tag: str) -> None:
        if tag.lower() == "title" and self._inside_title:
            self._inside_title = False
            self.page_title = " ".join("".join(self._title_chunks).split()).strip()

    def handle_data(self, data: str) -> None:
        if self._inside_title:
            self._title_chunks.append(data)


